// ── Helper functions ────────────────────────────────────────────────

/** Read all rows from a SQLite table */
function sqliteRows(table) {
  try { return sqlite.prepare(`SELECT * FROM "${table}"`).all(); }
  catch { return []; }
}

/** Check if a SQLite table exists */
function sqliteTableExists(table) {
  return Boolean(sqlite.prepare(
    "SELECT name FROM sqlite_master WHERE type='table' AND name=?"
  ).get(table));
}

/** Get the actual column names from PostgreSQL for a table */
async function pgColumns(client, table) {
  const res = await client.query(
    `SELECT column_name FROM information_schema.columns
     WHERE table_schema = 'public' AND table_name = $1`, [table]
  );
  return new Set(res.rows.map(r => r.column_name.toLowerCase()));
}

/**
 * Build an INSERT that maps SQLite columns → PG columns automatically.
 * Handles camelCase (SQLite) → lowercase (PG) mapping.
 * Only includes columns that actually exist in PG.
 */
function buildInsert(table, row, pgCols, conflictCol) {
  const pairs = [];
  for (const [col, val] of Object.entries(row)) {
    const lower = col.toLowerCase();
    if (pgCols.has(lower)) {
      pairs.push({ col: lower, val: val === undefined ? null : val });
    } else if (pgCols.has(col)) {
      pairs.push({ col, val: val === undefined ? null : val });
    }
  }
  if (!pairs.length) return null;

  const colList = pairs.map(p => `"${p.col}"`).join(", ");
  const placeholders = pairs.map((_, i) => `$${i + 1}`).join(", ");
  const values = pairs.map(p => p.val);

  return {
    sql: `INSERT INTO "${table}" (${colList}) VALUES (${placeholders})
          ON CONFLICT ("${conflictCol}") DO NOTHING`,
    values,
  };
}

/** Migrate one table: reads SQLite → inserts into PG */
async function migrateTable(client, table, conflictCol = "id") {
  if (!sqliteTableExists(table)) {
    console.log(`  ⏭  ${table}: not found in SQLite`);
    return;
  }
  const rows = sqliteRows(table);
  if (!rows.length) {
    console.log(`  ⏭  ${table}: empty`);
    return;
  }

  const pgCols = await pgColumns(client, table);
  if (!pgCols.size) {
    console.log(`  ⚠  ${table}: table not found in PostgreSQL`);
    return;
  }

  process.stdout.write(`  📦 ${table} (${rows.length} rows) ... `);
  let inserted = 0, skipped = 0, errors = 0;

  for (const row of rows) {
    const q = buildInsert(table, row, pgCols, conflictCol);
    if (!q) { errors++; continue; }
    try {
      const r = await client.query(q.sql, q.values);
      if ((r.rowCount ?? 0) > 0) inserted++; else skipped++;
    } catch (err) {
      errors++;
      if (errors <= 2) {
        process.stdout.write(`\n     ⚠ ${err.message}\n     `);
      }
    }
  }
  console.log(`✅ ${inserted} inserted | ${skipped} skipped | ${errors} errors`);
}

/** Reset PostgreSQL identity sequence to MAX(id)+1 */
async function resetSeq(client, table) {
  try {
    const s = await client.query(
      `SELECT pg_get_serial_sequence('"${table}"', 'id') AS seq`
    );
    if (s.rows[0]?.seq) {
      await client.query(
        `SELECT setval('${s.rows[0].seq}',
           COALESCE((SELECT MAX(id) FROM "${table}"), 0) + 1, false)`
      );
    }
  } catch { /* no sequence (TEXT PK) — ignore */ }
}

