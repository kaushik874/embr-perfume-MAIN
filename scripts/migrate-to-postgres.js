    await migrateTable(client, "orders",                 "id");       await resetSeq(client, "orders");
    await migrateTable(client, "order_items",            "id");       await resetSeq(client, "order_items");
    await migrateTable(client, "reviews",                "id");       await resetSeq(client, "reviews");
    await migrateTable(client, "customer_addresses",     "id");       await resetSeq(client, "customer_addresses");
    await migrateTable(client, "otp_codes",              "id");       await resetSeq(client, "otp_codes");
