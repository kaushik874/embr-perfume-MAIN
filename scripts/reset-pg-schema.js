#!/usr/bin/env node
/**
 * reset-pg-schema.js
 *
 * Drops all existing tables in the Neon PostgreSQL database and recreates
 * them with the correct schema from server/db.ts.
 *
 * ⚠️  WARNING: This drops ALL data in the PostgreSQL database.
 * Run BEFORE migrate-sqlite-to-pg.js.
 * The SQLite database (data/embr.db) is NOT touched.
 *
 * Usage:
 *   node scripts/reset-pg-schema.js
 */

import pg from "pg";
import dotenv from "dotenv";

dotenv.config();

const databaseUrl = process.env.DATABASE_URL;
if (!databaseUrl) {
  console.error("ERROR: DATABASE_URL is not set in .env");
  process.exit(1);
}

const { Pool } = pg;
const pool = new Pool({ connectionString: databaseUrl });

const DROP_ALL = `
  DROP TABLE IF EXISTS analytics_page_views CASCADE;
  DROP TABLE IF EXISTS analytics_sessions CASCADE;
  DROP TABLE IF EXISTS analytics_visitors CASCADE;
  DROP TABLE IF EXISTS footer_links CASCADE;
  DROP TABLE IF EXISTS footer_columns CASCADE;
  DROP TABLE IF EXISTS otp_codes CASCADE;
  DROP TABLE IF EXISTS admin_logs CASCADE;
  DROP TABLE IF EXISTS login_attempts CASCADE;
  DROP TABLE IF EXISTS contact_messages CASCADE;
  DROP TABLE IF EXISTS newsletter_subscribers CASCADE;
  DROP TABLE IF EXISTS site_content CASCADE;
  DROP TABLE IF EXISTS site_sections CASCADE;
  DROP TABLE IF EXISTS about_banner CASCADE;
  DROP TABLE IF EXISTS hero_banners CASCADE;
  DROP TABLE IF EXISTS customer_addresses CASCADE;
  DROP TABLE IF EXISTS reviews CASCADE;
  DROP TABLE IF EXISTS order_items CASCADE;
  DROP TABLE IF EXISTS orders CASCADE;
  DROP TABLE IF EXISTS product_images CASCADE;
  DROP TABLE IF EXISTS products CASCADE;
  DROP TABLE IF EXISTS webhook_events CASCADE;
  DROP TABLE IF EXISTS coupons CASCADE;
  DROP TABLE IF EXISTS users CASCADE;
`;

const CREATE_ALL = `
  CREATE TABLE users (
    id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    email TEXT NOT NULL UNIQUE,
    name TEXT NOT NULL,
    password_hash TEXT NOT NULL,
    created_at TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP,
    phone TEXT,
    role TEXT NOT NULL DEFAULT 'user',
    google_id TEXT
  );

  CREATE TABLE products (
    id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    slug TEXT NOT NULL UNIQUE,
    name TEXT NOT NULL,
    notes TEXT NOT NULL,
    description TEXT,
    price INTEGER NOT NULL,
    mrp INTEGER NOT NULL,
    image TEXT,
    featured INTEGER NOT NULL DEFAULT 0,
    discount_price INTEGER,
    stock INTEGER NOT NULL DEFAULT 0,
    sku TEXT,
    category TEXT,
    status TEXT NOT NULL DEFAULT 'published',
    tags TEXT,
    collection_type TEXT NOT NULL DEFAULT 'secondary',
    bestseller INTEGER NOT NULL DEFAULT 0,
    key_features TEXT,
    how_to_apply TEXT,
    legal_information TEXT,
    head_notes TEXT,
    heart_notes TEXT,
    base_notes TEXT,
    review TEXT
  );

  CREATE TABLE orders (
    id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_id INTEGER NOT NULL REFERENCES users(id),
    status TEXT NOT NULL DEFAULT 'pending',
    total_paise INTEGER NOT NULL,
    razorpay_order_id TEXT,
    razorpay_payment_id TEXT,
    created_at TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP,
    shipping_name TEXT,
    shipping_email TEXT,
    shipping_phone TEXT,
    shipping_address TEXT,
    shipping_city TEXT,
    shipping_pincode TEXT,
    tracking_number TEXT,
    paid_at TIMESTAMPTZ,
    shipping_state TEXT,
    shipping_house_number TEXT,
    shipping_street TEXT,
    shipping_area TEXT,
    shipping_landmark TEXT,
    shipping_alternate_phone TEXT,
    shipping_company_name TEXT,
    shipping_address_id INTEGER
  );

  CREATE TABLE order_items (
    id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    order_id INTEGER NOT NULL REFERENCES orders(id),
    product_id INTEGER NOT NULL REFERENCES products(id),
    quantity INTEGER NOT NULL,
    price_paise INTEGER NOT NULL
  );

  CREATE TABLE webhook_events (
    event_id TEXT PRIMARY KEY,
    created_at TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP
  );

  CREATE TABLE coupons (
    id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    code TEXT NOT NULL UNIQUE,
    discount_type TEXT NOT NULL,
    discount_value INTEGER NOT NULL,
    expiry_date TIMESTAMPTZ,
    usage_limit INTEGER,
    times_used INTEGER NOT NULL DEFAULT 0,
    status TEXT NOT NULL DEFAULT 'active',
    created_at TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP
  );

  CREATE TABLE reviews (
    id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    product_id INTEGER NOT NULL REFERENCES products(id),
    user_id INTEGER NOT NULL REFERENCES users(id),
    rating INTEGER NOT NULL,
    comment TEXT,
    status TEXT NOT NULL DEFAULT 'pending',
    created_at TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP,
    order_id INTEGER REFERENCES orders(id),
    title TEXT,
    images TEXT,
    video TEXT,
    reply TEXT,
    customer_name TEXT,
    customer_email TEXT,
    customer_phone TEXT,
    is_pinned INTEGER NOT NULL DEFAULT 0,
    is_featured INTEGER NOT NULL DEFAULT 0,
    is_hidden INTEGER NOT NULL DEFAULT 0
  );

  CREATE TABLE contact_messages (
    id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name TEXT NOT NULL,
    email TEXT NOT NULL,
    query TEXT NOT NULL,
    status TEXT NOT NULL DEFAULT 'new',
    created_at TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP
  );

  CREATE TABLE newsletter_subscribers (
    id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    email TEXT NOT NULL UNIQUE,
    subscribed_at TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP
  );

  CREATE TABLE site_content (
    key TEXT PRIMARY KEY,
    value TEXT NOT NULL
  );

  CREATE TABLE site_sections (
    key TEXT PRIMARY KEY,
    hidden INTEGER NOT NULL DEFAULT 0
  );

  CREATE TABLE login_attempts (
    id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    email TEXT NOT NULL,
    ip TEXT,
    success INTEGER NOT NULL DEFAULT 0,
    created_at TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP
  );

  CREATE TABLE admin_logs (
    id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_id INTEGER NOT NULL REFERENCES users(id),
    action TEXT NOT NULL,
    details TEXT,
    created_at TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP
  );

  CREATE TABLE customer_addresses (
    id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_id INTEGER NOT NULL REFERENCES users(id) ON DELETE CASCADE,
    full_name TEXT NOT NULL,
    mobile TEXT NOT NULL,
    email TEXT NOT NULL,
    house_number TEXT NOT NULL,
    street TEXT NOT NULL,
    area TEXT NOT NULL,
    city TEXT NOT NULL,
    state TEXT NOT NULL,
    pincode TEXT NOT NULL,
    landmark TEXT,
    alternate_mobile TEXT,
    company_name TEXT,
    is_default INTEGER NOT NULL DEFAULT 0,
    created_at TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP
  );

  CREATE TABLE otp_codes (
    id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_id INTEGER NOT NULL REFERENCES users(id) ON DELETE CASCADE,
    identifier TEXT NOT NULL,
    channel TEXT NOT NULL,
    otp_hash TEXT NOT NULL,
    expires_at TIMESTAMPTZ NOT NULL,
    consumed_at TIMESTAMPTZ,
    created_at TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP
  );

  CREATE TABLE product_images (
    id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    product_id INTEGER NOT NULL REFERENCES products(id) ON DELETE CASCADE,
    url TEXT NOT NULL,
    sort_order INTEGER NOT NULL DEFAULT 0,
    created_at TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP
  );

  CREATE TABLE hero_banners (
    id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    title TEXT,
    subtitle TEXT,
    description TEXT,
    imageurl TEXT NOT NULL,
    badge TEXT,
    buttontext TEXT,
    buttonlink TEXT,
    showtext INTEGER NOT NULL DEFAULT 1,
    isactive INTEGER NOT NULL DEFAULT 1,
    displayorder INTEGER NOT NULL DEFAULT 0,
    created_at TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP,
    productname TEXT,
    producturl TEXT,
    showbutton INTEGER NOT NULL DEFAULT 1,
    darkoverlay INTEGER NOT NULL DEFAULT 1,
    imagefit TEXT NOT NULL DEFAULT 'cover',
    imageposition TEXT NOT NULL DEFAULT 'center center',
    mobileimageposition TEXT NOT NULL DEFAULT 'center center'
  );

  CREATE TABLE about_banner (
    id INTEGER PRIMARY KEY CHECK (id = 1),
    title TEXT NOT NULL DEFAULT 'Our Story',
    subtitle TEXT NOT NULL DEFAULT 'Crafted in shadow. Worn in light.',
    description TEXT NOT NULL DEFAULT 'Embr began in a small atelier on the edge of a cedar forest.',
    imageurl TEXT NOT NULL DEFAULT '',
    showtext INTEGER NOT NULL DEFAULT 1,
    isactive INTEGER NOT NULL DEFAULT 1,
    darkoverlay INTEGER NOT NULL DEFAULT 1,
    buttontext TEXT NOT NULL DEFAULT '',
    buttonlink TEXT NOT NULL DEFAULT '',
    showbutton INTEGER NOT NULL DEFAULT 0,
    updated_at TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP
  );

  CREATE TABLE footer_columns (
    id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    title TEXT NOT NULL DEFAULT 'Column',
    sort_order INTEGER NOT NULL DEFAULT 0,
    created_at TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP
  );

  CREATE TABLE footer_links (
    id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    column_id INTEGER NOT NULL REFERENCES footer_columns(id) ON DELETE CASCADE,
    label TEXT NOT NULL,
    url TEXT NOT NULL DEFAULT '#',
    sort_order INTEGER NOT NULL DEFAULT 0
  );

  CREATE TABLE analytics_visitors (
    id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    visitor_id TEXT NOT NULL UNIQUE,
    first_seen TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP,
    last_seen TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP,
    visit_count INTEGER NOT NULL DEFAULT 1,
    device TEXT,
    browser TEXT,
    os TEXT,
    country TEXT,
    region TEXT,
    city TEXT,
    ip TEXT
  );

  CREATE TABLE analytics_sessions (
    id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    session_id TEXT NOT NULL UNIQUE,
    visitor_id TEXT NOT NULL,
    started_at TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP,
    last_seen TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP,
    duration_seconds INTEGER NOT NULL DEFAULT 0,
    landing_page TEXT,
    exit_page TEXT,
    referrer TEXT,
    referrer_source TEXT NOT NULL DEFAULT 'direct',
    pages_viewed INTEGER NOT NULL DEFAULT 0,
    is_bounce INTEGER NOT NULL DEFAULT 1
  );

  CREATE TABLE analytics_page_views (
    id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    session_id TEXT NOT NULL,
    visitor_id TEXT NOT NULL,
    page TEXT NOT NULL,
    created_at TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP
  );

  CREATE INDEX idx_orders_user_id ON orders(user_id);
  CREATE INDEX idx_reviews_product ON reviews(product_id);
  CREATE INDEX idx_reviews_user_product ON reviews(user_id, product_id);
  CREATE INDEX idx_analytics_sessions_started ON analytics_sessions(started_at);
  CREATE INDEX idx_analytics_sessions_last ON analytics_sessions(last_seen);
  CREATE INDEX idx_analytics_sessions_visitor ON analytics_sessions(visitor_id);
  CREATE INDEX idx_analytics_visitors_last ON analytics_visitors(last_seen);
  CREATE INDEX idx_analytics_pageviews_page ON analytics_page_views(page);
  CREATE INDEX idx_analytics_pageviews_session ON analytics_page_views(session_id);
  CREATE INDEX idx_analytics_pageviews_created ON analytics_page_views(created_at);
`;

async function main() {
  console.log("=".repeat(60));
  console.log("Embr PostgreSQL Schema Reset");
  console.log("=".repeat(60));
  console.log(`Target: ${databaseUrl.replace(/:[^:@]+@/, ":****@")}`);
  console.log("");
  console.log("⚠️  Dropping all existing tables...");

  const client = await pool.connect();
  try {
    await client.query(DROP_ALL);
    console.log("✓  All tables dropped.");

    console.log("  Recreating schema with correct structure...");
    await client.query(CREATE_ALL);
    console.log("✓  Schema created successfully.");

    console.log("");
    console.log("=".repeat(60));
    console.log("✅  Schema reset complete!");
    console.log("=".repeat(60));
    console.log("");
    console.log("Now run:  node scripts/migrate-sqlite-to-pg.js");
    console.log("");
  } catch (err) {
    console.error("\n[FATAL]", err.message);
    process.exit(1);
  } finally {
    client.release();
    await pool.end();
  }
}

main();
